    private void cargarTransacciones() {
        try {

            List<Transaccion> lista = transaccionDao.listar();

            DefaultTableModel modelo = (DefaultTableModel) tablaTransacciones.getModel();
            modelo.setRowCount(0);

            for (Transaccion t : lista) {
                modelo.addRow(new Object[]{
                    t.getIdTransaccion(),
                    t.getTipo(),
                    t.getFecha(),
                    t.getIdEmpleado(),
                    t.getIdActivo(),
                    t.getIdSucursal()
                });
            }

            if (lista.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No hay transacciones registradas.");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al cargar transacciones: " + ex.getMessage());
            ex.printStackTrace();
        }
    }

